# Python program that perform all compound operations
# -=,+=,*=,/=,%=

num=5
print("Given number is",num)

# Addition assignment operator
num+=4
print("After addition assignment operator:",num)

#Subtraction assignment operator
num-=4
print("After subtraction assignment operator:",num)

#Multiplication assignment operator
num*=4
print("After multiplication assignment operator:",num)

#Division assignment operator
num/=4
print("After division assignment operator:",num)

#Division assignment operator
num%=5
print("After divisioin assignment operator:",num)